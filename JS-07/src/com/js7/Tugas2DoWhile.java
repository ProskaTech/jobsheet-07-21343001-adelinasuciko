package com.js7;

public class Tugas2DoWhile {

	public static void main(String[] args) {
		
		int i=100;
		
		System.out.println("Program Menampilkan Angka 100-1 Looping Do-While");
		System.out.println("*************************************************");
		
		do {
			System.out.println(i);
			i--;
		}
		while(i>=1);
	}

}
