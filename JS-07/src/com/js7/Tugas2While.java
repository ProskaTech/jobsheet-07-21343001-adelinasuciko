package com.js7;

public class Tugas2While {

	public static void main(String[] args) {
		
		int i=100;
		
		System.out.println("Program Menampilkan Angka 100-1 Looping While");
		System.out.println("**************************************************");
		
		while(i>=1) {
			System.out.println(i);
			i--;
		}
	}
}
